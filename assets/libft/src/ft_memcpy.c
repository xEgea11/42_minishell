#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (!len || (!dest && !src))
		return (dest);
	while (len--)
		*d++ = *s++;
	return (dest);
}
